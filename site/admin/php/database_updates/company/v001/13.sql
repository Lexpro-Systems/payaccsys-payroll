--
-- CREATE sars_codes TABLE
--

CREATE TABLE sars_codes
(
	code				CHAR(4) PRIMARY KEY NOT NULL,
	description			VARCHAR NOT NULL,
	profit_loss			CHAR(1),
	start_year			INTEGER NOT NULL,
	end_year			INTEGER
);

--
